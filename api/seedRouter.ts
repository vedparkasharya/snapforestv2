export const seedRouter = createRouter({
  run: publicQuery.mutation(async () => {
    const db = getDb();

    const existingRooms = await db.select().from(rooms);
    if (existingRooms.length > 0) {
      return { message: "Data already seeded", count: existingRooms.length };
    }

    const [adminUser] = await db.insert(users).values({
      unionId: "admin_snapforest",
      name: "SNAPFOREST Admin",
      email: "admin@snapforest.com",
      role: "admin",
      isHost: true,
    }).$returningId();

    const [hostUser] = await db.insert(users).values({
      unionId: "host_snapforest",
      name: "Demo Host",
      email: "host@snapforest.com",
      role: "user",
      isHost: true,
    }).$returningId();

    for (const room of seedRoomsData) {
      await db.insert(rooms).values({
        ...room,
        hostId: hostUser.id,
      });
    }

    return {
      message: "Seed completed successfully",
      adminId: adminUser.id,
      hostId: hostUser.id,
      roomsCount: seedRoomsData.length,
    };
  }),
});
